package Questions;

public class DataParsingObj {
String jd;
String ed;
public DataParsingObj() {
	
}
public DataParsingObj(String jd, String ed) {
	super();
	this.jd = jd;
	this.ed = ed;
}
public String getJd() {
	return jd;
}
public void setJd(String jd) {
	this.jd = jd;
}
public String getEd() {
	return ed;
}
public void setEd(String ed) {
	this.ed = ed;
}
}
